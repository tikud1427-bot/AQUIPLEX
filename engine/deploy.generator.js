/**
 * generateDeployConfigs(brain, targets[]) → files[]
 *
 * @param {object}   brain   — project brain object
 * @param {string[]} targets — e.g. ["vercel","docker"] or ["auto"]
 * @returns {{ fileName, content, language }[]}
 */
function generateDeployConfigs(brain, targets = ["auto"]) {
  if (!brain) return [];

  const isFullstack = brain.isFullstack || false;
  const deployTarget = (brain.deployTarget || "render").toLowerCase();

  // "auto" = use brain's preferred target + docker always included for fullstack
  let resolvedTargets = targets.includes("auto")
    ? isFullstack
      ? [deployTarget, "docker"]
      : [deployTarget === "node" ? "netlify" : deployTarget]
    : targets;

  // Deduplicate
  resolvedTargets = [...new Set(resolvedTargets)];

  log.info(`generateDeployConfigs: isFullstack=${isFullstack} targets=[${resolvedTargets.join(",")}]`);

  const allFiles = [];
  const seen     = new Set();

  for (const target of resolvedTargets) {
    let generated = [];

    switch (target.toLowerCase()) {
      case "vercel":  generated = generateVercel(brain);  break;
      case "render":  generated = generateRender(brain);  break;
      case "railway": generated = generateRailway(brain); break;
      case "docker":  generated = generateDocker(brain);  break;
      case "netlify": generated = generateNetlify(brain); break;
      case "node":    generated = generateNode(brain);    break;
      default:
        log.warn(`Unknown deploy target: ${target}`);
        continue;
    }

    for (const f of generated) {
      if (!seen.has(f.fileName)) {
        seen.add(f.fileName);
        allFiles.push(f);
      }
    }
  }

  // Always include .gitignore
  const gitignoreFiles = generateGitignore(brain);
  for (const f of gitignoreFiles) {
    if (!seen.has(f.fileName)) {
      seen.add(f.fileName);
      allFiles.push(f);
    }
  }

  log.info(`generateDeployConfigs: generated ${allFiles.length} files: [${allFiles.map(f => f.fileName).join(", ")}]`);
  return allFiles;
}

/**
 * getAvailableTargets(brain) → string[]
 * Returns the relevant deploy targets for a given brain.
 */
function getAvailableTargets(brain) {
  if (!brain?.isFullstack) return ["vercel", "netlify", "render"];
  return ["vercel", "render", "railway", "docker"];
}

module.exports = {
  generateDeployConfigs,
  getAvailableTargets,
  generateVercel,
  generateRender,
  generateRailway,
  generateDocker,
  generateNetlify,
  generateNode,
  generateGitignore,
};
