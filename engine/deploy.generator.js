"use strict";

/**
 * engine/deploy.generator.js — AQUIPLEX DEPLOY CONFIG GENERATOR
 *
 * Generates deployment configuration files for fullstack and frontend projects.
 *
 * Supported targets:
 *   vercel   → vercel.json + .vercelignore
 *   render   → render.yaml
 *   railway  → railway.toml + Procfile
 *   docker   → Dockerfile + .dockerignore + docker-compose.yml
 *   netlify  → netlify.toml (frontend only)
 *   node     → Procfile + .nvmrc (minimal)
 *
 * All generators are pure functions (brain → files[]).
 * No AI calls — deterministic from brain data.
 */

const { createLogger } = require("../utils/logger");
const log = createLogger("DEPLOY_GEN");

// ─────────────────────────────────────────────────────────────────────────────
// HELPERS
// ─────────────────────────────────────────────────────────────────────────────

function envBlock(envVars = []) {
  if (!envVars.length) return "";
  return envVars
    .map(v => {
      const [key] = v.split("=");
      return `    - key: ${key.trim()}\n      value: ""\n      generateValue: false`;
    })
    .join("\n");
}

function dockerfileBase(stack = {}) {
  const db      = (stack.database || "none").toLowerCase();
  const hasDB   = db !== "none";
  const nodeVer = "20";

  return `FROM node:${nodeVer}-alpine

WORKDIR /app

# Install dependencies
COPY package*.json ./
RUN npm ci --only=production

# Copy source
COPY . .

# Expose port
EXPOSE $PORT

# Health check
HEALTHCHECK --interval=30s --timeout=5s --start-period=10s --retries=3 \\
  CMD node -e "require('http').get('http://localhost:' + (process.env.PORT||3000) + '/api/health', r => process.exit(r.statusCode === 200 ? 0 : 1))"

# Start
CMD ["node", "server.js"]
`;
}

function dockerignore() {
  return `node_modules
npm-debug.log
.env
.env.*
!.env.example
.git
.gitignore
*.md
coverage
.nyc_output
dist
build
.DS_Store
*.log
`;
}

