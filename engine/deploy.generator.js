function generateVercel(brain) {
  const files = [];
  const isFullstack = brain.isFullstack;
  const stack = brain.stack || {};

  if (isFullstack) {
    // Serverless Express via @vercel/node
    const vercelJson = {
      version: 2,
      builds: [
        { src: "server.js", use: "@vercel/node" }
      ],
      routes: [
        // API routes → server
        { src: "/api/(.*)", dest: "/server.js" },
        // Static assets
        { src: "/public/(.*)", dest: "/public/$1" },
        // SPA fallback
        { src: "/(.*)", dest: "/server.js" }
      ],
      env: Object.fromEntries(
        (brain.envVars || []).map(v => {
          const [k] = v.split("=");
          return [k.trim(), `@${k.trim().toLowerCase().replace(/_/g, "-")}`];
        })
      )
    };

    files.push({
      fileName: "vercel.json",
      content:  JSON.stringify(vercelJson, null, 2),
      language: "json",
    });

    files.push({
      fileName: ".vercelignore",
      content:  "node_modules\n.env\n*.log\ndata/\nprojects/\n",
      language: "plaintext",
    });
  } else {
    // Pure frontend — static deployment
    const vercelJson = {
      version: 2,
      builds: [
        { src: "index.html", use: "@vercel/static" }
      ],
      routes: [
        { src: "/(.*)", dest: "/$1" }
      ]
    };

    files.push({
      fileName: "vercel.json",
      content:  JSON.stringify(vercelJson, null, 2),
      language: "json",
    });
  }

  return files;
}

function generateRender(brain) {
  const files = [];
  const isFullstack = brain.isFullstack;
  const stack = brain.stack || {};
  const name  = (brain.name || "aquiplex-app")
    .toLowerCase().replace(/[^a-z0-9-]/g, "-").slice(0, 40);

  const envSection = (brain.envVars || []).length
    ? `  envVars:\n${envBlock(brain.envVars)}`
    : "";

  if (isFullstack) {
    const hasDB = stack.database && stack.database !== "none";

    let yaml = `# render.yaml — generated by AQUIPLEX
# https://render.com/docs/yaml-spec

services:
  - type: web
    name: ${name}
    runtime: node
    buildCommand: npm install
    startCommand: node server.js
    healthCheckPath: /api/health
    envVars:
      - key: NODE_ENV
        value: production
      - key: PORT
        value: 3000
`;

    if (brain.envVars?.length) {
      const extra = brain.envVars
        .filter(v => !v.startsWith("PORT=") && !v.startsWith("NODE_ENV="))
        .map(v => {
          const [k] = v.split("=");
          return `      - key: ${k.trim()}\n        sync: false`;
        })
        .join("\n");
      if (extra) yaml += extra + "\n";
    }

    if (hasDB && stack.database === "mongodb") {
      yaml += `\n# Note: use MongoDB Atlas — add MONGO_URI to env vars above\n`;
    }
    if (hasDB && stack.database === "postgres") {
      yaml += `\ndatabases:\n  - name: ${name}-db\n    databaseName: ${name.replace(/-/g, "_")}\n    user: ${name.replace(/-/g, "_")}\n`;
    }

    files.push({ fileName: "render.yaml", content: yaml, language: "plaintext" });
  } else {
    // Static site
    const yaml = `# render.yaml — generated by AQUIPLEX
services:
  - type: web
    name: ${name}
    runtime: static
    staticPublishPath: .
    buildCommand: echo "static site"
    headers:
      - path: /*
        name: Cache-Control
        value: public, max-age=3600
`;
    files.push({ fileName: "render.yaml", content: yaml, language: "plaintext" });
  }

  return files;
}

function generateRailway(brain) {
  const files = [];
  const isFullstack = brain.isFullstack;
  const stack = brain.stack || {};

  if (isFullstack) {
    const toml = `# railway.toml — generated by AQUIPLEX
# https://docs.railway.app/reference/config-as-code

[build]
builder = "NIXPACKS"

[deploy]
startCommand = "node server.js"
healthcheckPath = "/api/health"
healthcheckTimeout = 30
restartPolicyType = "ON_FAILURE"
restartPolicyMaxRetries = 3
`;
    files.push({ fileName: "railway.toml", content: toml, language: "plaintext" });
    files.push({ fileName: "Procfile", content: "web: node server.js\n", language: "plaintext" });
    files.push({ fileName: ".nvmrc", content: "20\n", language: "plaintext" });
  } else {
    files.push({ fileName: "Procfile", content: "web: npx serve . -l $PORT\n", language: "plaintext" });
    files.push({ fileName: ".nvmrc", content: "20\n", language: "plaintext" });
  }

  return files;
}

function generateDocker(brain) {
  const files = [];
  const stack = brain.stack || {};
  const isFullstack = brain.isFullstack;
  const db = (stack.database || "none").toLowerCase();
  const hasDB = db !== "none";

  files.push({
    fileName: "Dockerfile",
    content:  dockerfileBase(stack),
    language: "plaintext",
  });

  files.push({
    fileName: ".dockerignore",
    content:  dockerignore(),
    language: "plaintext",
  });

  if (isFullstack) {
    // docker-compose.yml — app + optional DB
    let composeServices = `  app:
    build: .
    ports:
      - "3000:3000"
    environment:
      - NODE_ENV=production
      - PORT=3000
`;

    // Add env vars
    if (brain.envVars?.length) {
      const filtered = brain.envVars
        .filter(v => !v.startsWith("PORT=") && !v.startsWith("NODE_ENV="))
        .map(v => {
          const [k] = v.split("=");
          return `      - ${k.trim()}=\${${k.trim()}}`;
        });
      if (filtered.length) {
        composeServices += filtered.join("\n") + "\n";
      }
    }

    if (hasDB) {
      if (db === "mongodb") {
        composeServices += `    depends_on:
      - mongo
    links:
      - mongo

  mongo:
    image: mongo:7
    ports:
      - "27017:27017"
    volumes:
      - mongo_data:/data/db
    environment:
      - MONGO_INITDB_DATABASE=app
`;
      } else if (db === "postgres" || db === "postgresql") {
        composeServices += `    depends_on:
      - postgres
    links:
      - postgres

  postgres:
    image: postgres:16-alpine
    ports:
      - "5432:5432"
    volumes:
      - pg_data:/var/lib/postgresql/data
    environment:
      - POSTGRES_DB=app
      - POSTGRES_USER=app
      - POSTGRES_PASSWORD=\${DB_PASSWORD:-changeme}
`;
      }
    }

    let volumes = "";
    if (db === "mongodb") volumes = "\nvolumes:\n  mongo_data:\n";
    if (db === "postgres" || db === "postgresql") volumes = "\nvolumes:\n  pg_data:\n";

    const compose = `# docker-compose.yml — generated by AQUIPLEX
version: "3.9"

services:
${composeServices}${volumes}`;

    files.push({ fileName: "docker-compose.yml", content: compose, language: "plaintext" });
  }

  return files;
}

function generateNetlify(brain) {
  // Frontend only
  const toml = `# netlify.toml — generated by AQUIPLEX
[build]
  publish = "."

[[headers]]
  for = "/*"
  [headers.values]
    Cache-Control = "public, max-age=3600"
    X-Frame-Options = "DENY"
    X-Content-Type-Options = "nosniff"

[[redirects]]
  from = "/*"
  to = "/index.html"
  status = 200
`;
  return [{ fileName: "netlify.toml", content: toml, language: "plaintext" }];
}

function generateNode(brain) {
  // Minimal — just Procfile + .nvmrc
  return [
    { fileName: "Procfile", content: "web: node server.js\n", language: "plaintext" },
    { fileName: ".nvmrc",   content: "20\n",                  language: "plaintext" },
  ];
}

// ─────────────────────────────────────────────────────────────────────────────
// SHARED: .gitignore (always useful)
// ─────────────────────────────────────────────────────────────────────────────

function generateGitignore(brain) {
  const isFullstack = brain.isFullstack;
  return [{
    fileName: ".gitignore",
    content: [
      "node_modules/",
      ".env",
      "*.env",
      "!.env.example",
      "*.log",
      "npm-debug.log*",
      ".DS_Store",
      "Thumbs.db",
      ...(isFullstack ? ["data/", "projects/", "uploads/", "dist/", "build/"] : []),
    ].join("\n") + "\n",
    language: "plaintext",
  }];
}

